			<h4>キズアトのふくらみを軽減するステロイドテープ</h4>
			<p>ステロイドのテープ(ドレニゾンテープやエクラーのブラスター)が用いられます。特に エクラー ブラスターの効果は高く、皮膚が厚い大人には大変有効です。</p><p>皮膚の薄い小児や高齢者にはドレニゾンRテープでも十分な効果が得られます。かぶれを生じなければ長く使用することで肥厚性瘢痕やケロイドの盛り上がりが改善します。</p>
			<p>&nbsp;</p>

			<h4>ケロイド状の傷跡のふくらみをへこませるケナコルト注射</h4>
		<p>ステロイド(ケナコルト R)を注射することがあります。赤みや盛り上がり、痛みや痒みは速やかに軽減します。</p><p>効果が強すぎるとかえってんだ瘢痕になることもあります。塗り薬と同じく、ステロイドであるため、周囲の皮膚が薄くなって毛細血管が拡張することも欠点です。</p><p>また硬い瘢痕の中に注射すると痛みが出るため、痛くない注射・効果的な 注射には熟練の技術が必要です。女性ではステロイドの影響で生理不順が生じること もあるため注意が必要です。</p>
			<p>&nbsp;</p>

