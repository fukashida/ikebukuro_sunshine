		<h3 class="subTitle" id="toc6-3">内服薬　外用薬</h3>
		<div class="medicine">
			<h4>ケロイド予防のリザベン</h4>
<p>飲み薬ではトラニラスト(リザベンR)が有効であるとされています。これは抗アレルギー剤であり、肥厚性瘢痕やケロイドの組織中にある各種炎症細胞が出す化学伝達物質を 抑制することにより、痒みをはじめとする自覚症状を抑え、さらには病変自体を沈静化 させると考えられているものです。</p><p>また漢方薬の柴苓湯が使われることもあります。これらの効果は強くないので、他の治療法と合わせて用いられます。</p>
