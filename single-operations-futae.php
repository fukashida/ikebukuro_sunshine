<section class="secBox clrfix">
<h4 class="subTitle">●上まぶたのタルミ取り</h4>
<p class="none">上まぶたのタルミ取りは、二重のライン上を切開し弛んだ部分を取り除いた後、皮膚と筋肉を引き上げる方法です。</p><p class="mb10"><span class="bold att">目元の重たさを改善し、スッキリとした目元</span>を手に入れることができます。</p><p class="mb10">まぶたの重さや二重のかぶさりが解消されるため、見た目も見違えるほど若返ります。</p><p class="mb10"> もちろん一重のままをご希望の方には、たるみのみを取ることも可能です。</p><p class="mb10">その場合、まつ毛の生え際の1～2mm程上をカットしてタルミのみを解消します。</p><p class="mb10">術後直後の目のハレが気になる方は、個室をご用意しゆっくりとアイスで冷やしながらお休み頂けます。</p><p class="mb10">ご来院の際には、サングラスをご用意されることをオススメいたします。</p><p class="mb10">7日後が抜糸となり、キズ跡もほとんどありません。</p><p class="mb10">また、軽いタルミの方は切らない手術の埋没法で直すこともできます。</p><p class="mb10">
※保険が適用される場合があります。</p><p class="mb10"></p>
</section>
<section class="secBox clrfix">
<h4 class="subTitle">●下まぶたのタルミ取り</h4>
<p class="none">下まぶたのタルミ取りは、下まつ毛の生え際1～2mm下をカットし<span class="att">余分な脂肪と余分な皮膚を取り除く</span>ことによって、<span class="bold att">目元をスッキリ</span>させます。</p><p class="mb10">キズ跡はほとんど残らず、下まつ毛に隠れてしまいます。</p><p class="mb10">タルミを取ることが目的となりますので目の形が変わったり下がったりする心配もございません。</p><p class="mb10"> 目の下のタルミがなくなることで万年疲れ顔状態から開放されます。</p><p class="mb10">上まぶたのタルミ取り同様、術後直後の目のハレが気になる方は、個室をご用意いたします。</p><p class="mb10">ゆっくりとアイスで冷やしながらお休み頂けます。</p><p class="mb10">ご来院の際には、サングラスをご用意されることをオススメいたします。</p><p class="mb10">「目のクマ」も同時に解消され、若返りの効果もあります。</p><p class="mb10">
※目の下のタルミは、ヒアルロン酸注入でも軽減可能です。</p><p class="mb10">カウンセリング時にご相談下さいませ。</p><p class="mb10"></p>
</section>
