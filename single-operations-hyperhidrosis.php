		<h2 class="baseTitle" id="toc1">気になる汗ジミ、これって多汗症？症状の紹介</h2>

<p>運動した時や辛い物を食べた時、緊張した時などに私たちの体は汗をかきます。</p><p>しかし、「汗をかかないような状況でも汗が止まらない」「人より汗が多い気がする」と悩んでいませんか。</p><p>もしかしたら多汗症かもしれませんよ。</p>

		<section class="secBox">
			<h3 class="subTitle" id="toc1-1">そもそも多汗症とは？</h3>

<p>人間の体は、温度を一定に保つために汗をかいて体温を下げます。発汗するタイミングは3つあります。</p>
<br>
<p><b>①温熱性発汗</b>：運動や気温が高い状況で体の熱を逃がすために汗が出る<br>
<b>②味覚性発汗</b>：辛味や熱いものを食べている時に汗が出る<br>
<b>③精神性発汗</b>：人前での発言や苦手なことなど緊張する時に汗が出る</p>
<br>
<p>一般的には、この3つの状態が終わってしばらくすれば汗はひきます。しかし、<span class="att"><strong>3つの状態には当てはまらない状況でも汗が止まらない症状を多汗症と呼びます</strong></span>。</p>

<h3 class="subTitle" id="toc1-2">多汗症の種類について</h3>
<p>多汗症には全身から汗が出る「全身性多汗症」と体の一部から汗が出る「局所性多汗症」に分類されます。</p>


<div class="takansyoPrice mb20">
				<table>
					<tbody><tr><th></th><th>全身性多汗症</th><th>局所性多汗症</th></tr>
					<tr><td>汗が出る部位</td><td>全身</td><td>汗が出る部位	全身	脇・手のひら・足の裏など</td></tr>
					<tr><td>原因</td><td>感染症・内分泌代謝異常・神経疾患</td><td>精神的な緊張・末梢神経の損傷</td></tr>
				</tbody></table>
			</div>

<p>このほかに薬の副作用による多汗症の可能性もあります。薬を日常的に服用されている場合は、処方されたかかりつけ医に一度相談してみてください。</p><p>
また、上記の状態に当てはまらず多汗症の原因が不明な場合もあり「原発性多汗症」と呼ばれます。</p>


<h3 class="subTitle" id="toc1-3">当院で扱う多汗症</h3>
<p>当院では、原因が分からない局所性多汗症の治療を行っています。自分が多汗症かどうか判断する基準は以下の6点です。</p>
<br>
<p>
①	最初に症状が出たのは25歳以下<br>
②	発汗する部位は左右で同じ<br>
③	寝ている時は発汗しない<br>
④	週に1回以上多汗することがある<br>
⑤	家族にも似たような症状がある<br>
⑥	多汗によって日常生活に支障がある<br>
</p>
<br>
<p>
以上の6項目のうち、2項目以上当てはまっている場合は多汗症の可能性が高いです。</p><p>多汗症が進行すると、脇汗によるワキガなどの臭いや汗のかき過ぎによる末端冷え性を併発することもあります。</p><p>多汗症かもしれない、汗の臭いが気になるなどのお悩みがある場合は、一度当院にご相談ください。</p><p>脇汗や手汗は日常生活にも大きくかかわる症状ですのえ、早めの治療をおすすめします。</p>


